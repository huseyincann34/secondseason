public class Obje {
    public  int x;
    public  int y;
    public  int puan;

    public Obje(int x, int y, int puan) {
        this.x = x;
        this.y = y;
        this.puan = puan;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getPuan() {
        return puan;
    }

    public void setPuan(int puan) {
        this.puan = puan;
    }
}

